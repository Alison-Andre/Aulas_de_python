# Operação aritmética

# Soma
soma = 1 + 1
print("1 + 1 =", soma)

# Subtração
subtracao = 3 - 2
print("3 - 2 =", subtracao)

# Multiplicação
multplicacao = 2 * 2
print("2 * 2 =", multplicacao)

# Divisao
divisao = 5 / 2
print("5 / 2 =", divisao)
# para divisão inteira utilizamos duas barras //

# Converter em inteiro
print("O valor inteiro é:", int(divisao))

# float() para converter para um número flutuante

# Módulo
modulo = 5 % 2
print("O resto da divisão de 5/2 é:", modulo)
